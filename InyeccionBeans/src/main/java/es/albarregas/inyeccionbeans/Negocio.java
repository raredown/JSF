package es.albarregas.inyeccionbeans;

/**
 *
 * @author Daw2
 */
public class Negocio {

    public static float sumar(int primero, int segundo) {
        return primero + segundo;
    }

    public static float restar(int primero, int segundo) {
        return primero - segundo;
    }

    public static float multiplicar(int primero, int segundo) {
        return primero * segundo;
    }

    public static float division(int primero, int segundo) {
        return primero / segundo;
    }
}
